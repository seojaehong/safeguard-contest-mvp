type ZipEntry = {
  path: string;
  content: string;
};

function sanitizeFileName(value: string) {
  return value.replace(/[\\/:*?"<>|]/g, "-").replace(/\s+/g, "-").slice(0, 80) || "safeguard";
}

function downloadBlob(blob: Blob, fileName: string) {
  const url = URL.createObjectURL(blob);
  const link = document.createElement("a");
  link.href = url;
  link.download = fileName;
  document.body.appendChild(link);
  link.click();
  link.remove();
  URL.revokeObjectURL(url);
}

function escapeHtml(value: string) {
  return value
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

function buildHtml(title: string, body: string) {
  return `<!doctype html>
<html lang="ko">
<head>
  <meta charset="utf-8" />
  <title>${escapeHtml(title)}</title>
  <style>
    body { font-family: "Noto Sans KR", "Malgun Gothic", sans-serif; color: #1d2430; margin: 40px; line-height: 1.65; }
    h1 { font-size: 24px; margin: 0 0 18px; }
    pre { white-space: pre-wrap; font-family: inherit; font-size: 14px; }
    .meta { color: #657084; font-size: 12px; margin-bottom: 22px; }
  </style>
</head>
<body>
  <h1>${escapeHtml(title)}</h1>
  <div class="meta">Generated by SafeGuard</div>
  <pre>${escapeHtml(body)}</pre>
</body>
</html>`;
}

function crc32(bytes: Uint8Array) {
  let crc = 0xffffffff;
  for (const byte of bytes) {
    crc ^= byte;
    for (let index = 0; index < 8; index += 1) {
      crc = (crc >>> 1) ^ (0xedb88320 & -(crc & 1));
    }
  }
  return (crc ^ 0xffffffff) >>> 0;
}

function writeUint16(target: number[], value: number) {
  target.push(value & 0xff, (value >>> 8) & 0xff);
}

function writeUint32(target: number[], value: number) {
  target.push(value & 0xff, (value >>> 8) & 0xff, (value >>> 16) & 0xff, (value >>> 24) & 0xff);
}

function appendBytes(target: number[], bytes: Uint8Array) {
  bytes.forEach((byte) => target.push(byte));
}

function createZip(entries: ZipEntry[]) {
  const encoder = new TextEncoder();
  const output: number[] = [];
  const centralDirectory: number[] = [];

  for (const entry of entries) {
    const pathBytes = encoder.encode(entry.path);
    const contentBytes = encoder.encode(entry.content);
    const checksum = crc32(contentBytes);
    const offset = output.length;

    writeUint32(output, 0x04034b50);
    writeUint16(output, 20);
    writeUint16(output, 0x0800);
    writeUint16(output, 0);
    writeUint16(output, 0);
    writeUint16(output, 0);
    writeUint32(output, checksum);
    writeUint32(output, contentBytes.length);
    writeUint32(output, contentBytes.length);
    writeUint16(output, pathBytes.length);
    writeUint16(output, 0);
    appendBytes(output, pathBytes);
    appendBytes(output, contentBytes);

    writeUint32(centralDirectory, 0x02014b50);
    writeUint16(centralDirectory, 20);
    writeUint16(centralDirectory, 20);
    writeUint16(centralDirectory, 0x0800);
    writeUint16(centralDirectory, 0);
    writeUint16(centralDirectory, 0);
    writeUint16(centralDirectory, 0);
    writeUint32(centralDirectory, checksum);
    writeUint32(centralDirectory, contentBytes.length);
    writeUint32(centralDirectory, contentBytes.length);
    writeUint16(centralDirectory, pathBytes.length);
    writeUint16(centralDirectory, 0);
    writeUint16(centralDirectory, 0);
    writeUint16(centralDirectory, 0);
    writeUint16(centralDirectory, 0);
    writeUint32(centralDirectory, 0);
    writeUint32(centralDirectory, offset);
    appendBytes(centralDirectory, pathBytes);
  }

  const centralOffset = output.length;
  output.push(...centralDirectory);
  writeUint32(output, 0x06054b50);
  writeUint16(output, 0);
  writeUint16(output, 0);
  writeUint16(output, entries.length);
  writeUint16(output, entries.length);
  writeUint32(output, centralDirectory.length);
  writeUint32(output, centralOffset);
  writeUint16(output, 0);

  return new Blob([new Uint8Array(output)], { type: "application/zip" });
}

function buildHwpx(title: string, body: string) {
  const lines = body.split("\n").map((line) => `<p>${escapeHtml(line || " ")}</p>`).join("");
  const entries: ZipEntry[] = [
    {
      path: "mimetype",
      content: "application/hwp+zip"
    },
    {
      path: "version.xml",
      content: `<?xml version="1.0" encoding="UTF-8"?><version app="SafeGuard" version="1.0" />`
    },
    {
      path: "Contents/content.hpf",
      content: `<?xml version="1.0" encoding="UTF-8"?><package><metadata><title>${escapeHtml(title)}</title></metadata><manifest><item href="section0.xml" media-type="text/xml" /></manifest></package>`
    },
    {
      path: "Contents/section0.xml",
      content: `<?xml version="1.0" encoding="UTF-8"?><section><h1>${escapeHtml(title)}</h1>${lines}</section>`
    }
  ];

  return createZip(entries);
}

function buildCombinedText(values: Record<DocumentKey, string>) {
  return documentMeta.map((item) => `# ${item.title}\n\n${values[item.key]}`).join("\n\n---\n\n");
}

export function WorkpackEditor({ data }: { data: AskResponse }) {
  const initialValues = useMemo<Record<DocumentKey, string>>(
    () => ({
      riskAssessmentDraft: data.deliverables.riskAssessmentDraft,
      tbmBriefing: data.deliverables.tbmBriefing,
      tbmLogDraft: data.deliverables.tbmLogDraft,
      safetyEducationRecordDraft: data.deliverables.safetyEducationRecordDraft,
      kakaoMessage: data.deliverables.kakaoMessage
    }),
    [data]
  );
  const [selectedKey, setSelectedKey] = useState<DocumentKey>("riskAssessmentDraft");
  const [values, setValues] = useState<Record<DocumentKey, string>>(initialValues);
  const selected = documentMeta.find((item) => item.key === selectedKey) || documentMeta[0];
  const selectedText = values[selected.key];
  const baseName = sanitizeFileName(`${data.scenario.companyName}-${selected.fileBase}`);

  function updateValue(value: string) {
    setValues((current) => ({ ...current, [selected.key]: value }));
  }

  function downloadText() {
    downloadBlob(new Blob([selectedText], { type: "text/plain;charset=utf-8" }), `${baseName}.txt`);
  }

  function downloadJson() {
    const payload = {
      title: selected.title,
      scenario: data.scenario,
      document: selectedText,
      generatedAt: new Date().toISOString()
    };
    downloadBlob(new Blob([JSON.stringify(payload, null, 2)], { type: "application/json;charset=utf-8" }), `${baseName}.json`);
  }

  function downloadHtml() {
    downloadBlob(new Blob([buildHtml(selected.title, selectedText)], { type: "text/html;charset=utf-8" }), `${baseName}.html`);
  }

  function downloadJpg() {
    const svg = `<svg xmlns="http://www.w3.org/2000/svg" width="1240" height="1754">
      <rect width="100%" height="100%" fill="#fffaf1"/>
      <foreignObject x="60" y="60" width="1120" height="1634">
        <div xmlns="http://www.w3.org/1999/xhtml" style="font-family: 'Malgun Gothic', sans-serif; color:#1d2430;">
          <h1 style="font-size:36px;margin:0 0 24px;">${escapeHtml(selected.title)}</h1>
          <pre style="white-space:pre-wrap;font-family:inherit;font-size:22px;line-height:1.55;">${escapeHtml(selectedText)}</pre>
        </div>
      </foreignObject>
    </svg>`;
    const image = new Image();
    const svgUrl = URL.createObjectURL(new Blob([svg], { type: "image/svg+xml;charset=utf-8" }));
    image.onload = () => {
      const canvas = document.createElement("canvas");
      canvas.width = 1240;
      canvas.height = 1754;
      const context = canvas.getContext("2d");
      if (!context) return;
      context.fillStyle = "#fffaf1";
      context.fillRect(0, 0, canvas.width, canvas.height);
      context.drawImage(image, 0, 0);
      canvas.toBlob((blob) => {
        if (blob) downloadBlob(blob, `${baseName}.jpg`);
        URL.revokeObjectURL(svgUrl);
      }, "image/jpeg", 0.92);
    };
    image.src = svgUrl;
  }

  function downloadHwpx() {
    downloadBlob(buildHwpx(selected.title, selectedText), `${baseName}.hwpx`);
  }

  function downloadAll() {
    const combined = buildCombinedText(values);
    downloadBlob(new Blob([combined], { type: "text/plain;charset=utf-8" }), `${sanitizeFileName(data.scenario.companyName)}-safeguard-workpack.txt`);
  }

  function printPdf() {
    const popup = window.open("", "_blank", "width=900,height=1100");
    if (!popup) return;
    popup.document.write(buildHtml(selected.title, selectedText));
    popup.document.close();
    popup.focus();
    popup.print();
  }

  return (
    <section className="workpack-shell" id="workpack">
      <div className="workpack-sidebar card list">
        <div>
          <div className="eyebrow">SafeGuard Workpack</div>
          <div className="h2">오늘 문서팩</div>
          <p className="muted">현장에서 바로 수정하고 내려받을 수 있는 작업 전 산출물입니다.</p>
        </div>
        <div className="doc-tab-list">
          {documentMeta.map((item) => (
            <button
              key={item.key}
              type="button"
              className={`doc-tab ${item.key === selected.key ? "active" : ""}`}
              onClick={() => setSelectedKey(item.key)}
            >
              <strong>{item.title}</strong>
              <span>{item.description}</span>
            </button>
          ))}
        </div>
        <button type="button" className="button secondary" onClick={downloadAll}>전체 TXT 다운로드</button>
      </div>

      <div className="card document-editor">
        <div className="document-toolbar">
          <div>
            <div className="eyebrow">Editable document</div>
            <div className="h2">{selected.title}</div>
            <p className="muted">{selected.description}</p>
          </div>
          <div className="download-bar">
            <button type="button" className="button secondary" onClick={downloadText}>TXT</button>
            <button type="button" className="button secondary" onClick={downloadJson}>JSON</button>
            <button type="button" className="button secondary" onClick={downloadHtml}>HTML</button>
            <button type="button" className="button secondary" onClick={downloadJpg}>JPG</button>
            <button type="button" className="button secondary" onClick={printPdf}>PDF</button>
            <button type="button" className="button" onClick={downloadHwpx}>HWPX</button>
          </div>
        </div>
        <textarea
          className="document-textarea"
          value={selectedText}
          onChange={(event) => updateValue(event.target.value)}
          aria-label={`${selected.title} 편집`}
        />
      </div>
    </section>
  );
}
